package com.rdebernard.phanes;

public class Vec3d{
  public float x;
  public float y;
  public float z;
  public Vec3d(){
    x=0;
    y=0;
    z=0;
  }
  public Vec3d(float x,float y){
    this.x = x;
    this.y = y;
  }
  public Vec3d(float x,float y,float z){
    this.x = x;
    this.y = y;
    this.z = z;
  }
}

